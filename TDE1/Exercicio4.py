# Entradas
valorDeProduto = float(input('Insira o valor do produto: '))
# Processamento
valorAVista = valorDeProduto * 0.95
valorDuasVezes = valorDeProduto / 2
valorTresVezes = (valorDeProduto * 1.05) / 3
# Saída
print('O valor a vista é de: ', valorAVista)
print('O valor em duas vezes é de: ', valorDuasVezes)
print('O valor em três vezes com acréssimo: ', valorTresVezes)