# Construa a tabela de multiplicação de 1 a 10 utilizando apenas um laço de repetição.
print('\n Tabela de multiplicação: ')
for n in range(1,11):
    print(f'1 * {n} = {1 *n}')
    print(f'2 * {n} = {2 * n}')
    print(f'3 * {n} = {3 * n}')
    print(f'4 * {n} = {4 * n}')
    print(f'5 * {n} = {5 * n}')
    print(f'6 * {n} = {6 * n}')
    print(f'7 * {n} = {7 * n}')
    print(f'8 * {n} = {8 * n}')
    print(f'9 * {n} = {9 * n}')
    print(f'10 * {n} = {10 * n}')